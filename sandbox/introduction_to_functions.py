def add(a, b):
    mysum = a + b
    return mysum

sum_of_num = add(6, 14)
print(sum_of_num)

def substract(a, b):
    mydifference = a - b
    return mydifference
difference_of_num = substract(24, 6)
print(difference_of_num)

def multiply(a, b):
    myproduct = a * b
    return myproduct
product_of_num = multiply(13, 12)
print(product_of_num)